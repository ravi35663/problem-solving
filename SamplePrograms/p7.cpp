// C Program to Check if Two Numbers are Equal
#include<iostream>
using namespace std;
int main(){
    int num1 ,num2;
    cout<<"Enter two numbers"<<endl;
    cin>>num1>>num2;
    if(num1==num2){
        cout<<"Both numbers are equal "<<endl;
    }else{
        cout<<"Numbers are not equal"<<endl;
    }
    return 1;

}